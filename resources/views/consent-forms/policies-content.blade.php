<h2>Gracimor Hyndland Estate Policies & Consent Agreement</h2>

<h3>1. Check-In / Check-Out Policy</h3>
<p>Check-in time is 14:00 (2:00 PM) and check-out time is 10:00 (10:00 AM). Early check-in or late check-out may be arranged subject to availability and may incur additional charges.</p>

<h3>2. Identification</h3>
<p>All guests are required to present a valid form of identification (National ID, Passport, or Driver's License) at the time of check-in. A copy may be retained for record-keeping purposes.</p>

<h3>3. Payment Policy</h3>
<p>Full payment is required prior to or at check-in unless otherwise agreed. Accepted payment methods include cash, mobile money, and bank transfer. A receipt will be provided for all payments made.</p>

<h3>4. Visitor Policy</h3>
<p>Visitors are allowed in common areas only between 08:00 and 20:00. Overnight visitors are not permitted without prior approval and may be subject to additional charges. All visitors must be registered at reception.</p>

<h3>5. Noise & Conduct</h3>
<p>Guests are expected to maintain a peaceful environment. Quiet hours are observed from 22:00 to 06:00. Excessive noise, disruptive behavior, or any activity that disturbs other guests may result in immediate eviction without refund.</p>

<h3>6. Smoking & Alcohol</h3>
<p>Smoking is strictly prohibited inside all rooms and enclosed common areas. Designated smoking areas are provided outside. Consumption of alcohol is permitted in moderation within private rooms only.</p>

<h3>7. Property & Damages</h3>
<p>Guests are responsible for any damage to the property, furniture, fixtures, or equipment during their stay. The cost of repair or replacement will be charged to the guest. Please report any pre-existing damage at the time of check-in.</p>

<h3>8. Housekeeping</h3>
<p>Daily housekeeping is provided. Guests are expected to maintain basic cleanliness in their rooms. Additional cleaning services may be requested at an extra charge.</p>

<h3>9. Security</h3>
<p>Gracimor Properties provides basic security measures. However, we are not responsible for the loss or theft of personal belongings. Guests are advised to use the safe provided or keep valuables secured. Please ensure doors and windows are locked when leaving your room.</p>

<h3>10. Key / Access Card Policy</h3>
<p>Keys or access cards issued at check-in must be returned at check-out. Loss or damage to keys/cards will incur a replacement fee.</p>

<h3>11. Pets</h3>
<p>Pets are not allowed on the premises unless prior written approval has been obtained. Where permitted, additional cleaning fees may apply.</p>

<h3>12. Internet & Utilities</h3>
<p>Wi-Fi is provided for guest use. Gracimor Properties is not responsible for internet outages or speed fluctuations. Guests are expected to use utilities (water, electricity) responsibly.</p>

<h3>13. Cancellation & Refund Policy</h3>
<p>Cancellations made more than 14 days before check-in will receive a full refund. Cancellations made 7-14 days before check-in will receive a 50% refund. Cancellations made less than 7 days before check-in are non-refundable. No-shows will be charged the full amount.</p>

<h3>14. Liability</h3>
<p>Gracimor Properties shall not be liable for any injury, loss, or damage sustained by the guest during their stay, except where caused by the negligence of Gracimor Properties management or staff.</p>

<h3>15. Right of Admission</h3>
<p>Management reserves the right to refuse admission or to evict any guest who violates these policies, engages in illegal activities, or poses a threat to the safety and comfort of other guests or staff.</p>

<h3>16. Data Protection</h3>
<p>Personal information collected during the booking and check-in process will be stored securely and used only for the purposes of managing your stay. Your data will not be shared with third parties without your consent, except as required by law.</p>

